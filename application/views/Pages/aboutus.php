                <p><span style="font-size: 14pt;"><strong>Crestbridge Savings and Loans </strong></span></p>
<p>Established in October 2003 Crestbridge set out to rediscover finance. Since then our services has extended international, covering the innovation gap in the financial world.</p>
<p>With over 700,000 members and over 1,000,000 personal and corporate accounts, <strong>  </strong></p>
<p><span style="font-size: 11pt;"><strong>Our goals will always be:</strong></span></p>
<ul>
<li style="text-align: left;">To be your Bank</li>
<li style="text-align: left;">To make a difference in your everyday life</li>
<li style="text-align: left;">To put our people (you) at the heart of everything we do</li>
<li style="text-align: left;">To provide uncomplicated financial services</li>
<li style="text-align: left;">To encourage healthy saving habits</li>
<li style="text-align: left;">To offer and provide low cost loans to our customers</li>
</ul>
<p><strong>Our &ldquo;Mission Statement&rdquo;</strong></p>
<p>Crestbridge is on a wind of innovation, spreading easy financial services to our clients who now have control over their finances, and whose finances can now support them to achieve their goals and to live well in a supportive society, thanks to our financial ecosystem.</p>
<p>The Board of Directors, Management, Staff and volunteers of the Crestbridge has affirmed to protect and direct the efforts of our loyal clients towards consistent and professional accomplishment of this mission.</p>
<p>Internationally over a 700,000 people recognizes the value of our services and this have amounted to more than 1 Million active accounts, with savings and investments of over 3 Billion pounds, with thousands employed in various sectors and thousands of volunteers involved.</p>
